import { COMMANDS, type VimCommand } from './commands'
import { WORLDS } from '../content/tiers'
import type { Tier } from './types'

/**
 * The downloadable / printable Vim cheatsheet — generated from the SAME command
 * catalog the game teaches (src/game/commands.ts), so it can never drift from
 * the curriculum. Rendered to Markdown (universal, diff-friendly) and to a
 * fully self-contained HTML page (open in a browser → File ▸ Print ▸ Save as PDF).
 */

const CATEGORY_LABEL: Record<string, string> = {
  modes: 'Modes',
  motion: 'Motion',
  edit: 'Edits',
  operator: 'Operators',
  'text-object': 'Text objects',
  ex: 'Ex commands',
  power: 'Power',
}
const CATEGORY_ORDER = ['modes', 'motion', 'edit', 'operator', 'text-object', 'ex', 'power']

export interface CheatSection {
  tier: Tier
  name: string
  subtitle: string
  accent: string
  groups: Array<{ label: string; commands: VimCommand[] }>
}

/** Commands grouped by world (tier), then by category — the cheatsheet spine. */
export function cheatsheetSections(): CheatSection[] {
  return WORLDS.map((w) => {
    const inWorld = COMMANDS.filter((c) => c.tier === w.tier)
    const groups = CATEGORY_ORDER.map((key) => ({
      label: CATEGORY_LABEL[key] ?? key,
      commands: inWorld.filter((c) => c.category === key),
    })).filter((g) => g.commands.length > 0)
    return { tier: w.tier, name: w.name, subtitle: w.subtitle, accent: w.accent, groups }
  }).filter((s) => s.groups.length > 0)
}

/** cheatsheetSections() narrowed to a free-text query over command keys + labels.
 *  Empty groups/sections are dropped so only matching worlds render. */
export function filterCheatsheetSections(query: string): CheatSection[] {
  const q = query.trim().toLowerCase()
  if (!q) return cheatsheetSections()
  return cheatsheetSections()
    .map((s) => ({
      ...s,
      groups: s.groups
        .map((g) => ({ ...g, commands: g.commands.filter((c) => c.keys.toLowerCase().includes(q) || c.label.toLowerCase().includes(q)) }))
        .filter((g) => g.commands.length > 0),
    }))
    .filter((s) => s.groups.length > 0)
}

const SITE = 'https://github.com/Zeecka/VimLegends'
const TAGLINE = 'Learn Vim by playing — real editor, real keystrokes, real muscle memory.'

// ------------------------------------------------------------- Markdown ---

export function cheatsheetMarkdown(): string {
  const lines: string[] = []
  lines.push('# VimLegends — Vim Cheatsheet', '', `> ${TAGLINE}`, '', `_${SITE}_`, '')
  for (const s of cheatsheetSections()) {
    lines.push(`## World ${s.tier} · ${s.name}`, `_${s.subtitle}_`, '')
    for (const g of s.groups) {
      lines.push(`**${g.label}**`, '', '| Keys | What it does |', '| --- | --- |')
      for (const c of g.commands) lines.push(`| \`${c.keys}\` | ${c.label} |`)
      lines.push('')
    }
  }
  lines.push('---', '', `${COMMANDS.length} commands across ${cheatsheetSections().length} worlds · generated by VimLegends.`)
  return lines.join('\n')
}

// ----------------------------------------------------------------- HTML ---

const esc = (s: string) => s.replace(/[&<>"]/g, (ch) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;' })[ch]!)

