// ------------------------------------------------------------- download ---

/** Trigger a browser download of an in-memory string as a file. */
export function downloadTextFile(filename: string, mime: string, content: string): void {
  if (typeof document === 'undefined' || typeof URL.createObjectURL !== 'function') return
  const blob = new Blob([content], { type: mime })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  a.rel = 'noopener'
  document.body.appendChild(a)
  a.click()
  a.remove()
  window.setTimeout(() => URL.revokeObjectURL(url), 2000)
}

export type CheatFormat = 'md' | 'html'

export function downloadCheatsheet(format: CheatFormat): void {
  if (format === 'md') {
    downloadTextFile('vimlegends-cheatsheet.md', 'text/markdown;charset=utf-8', cheatsheetMarkdown())
  } else {
    downloadTextFile('vimlegends-cheatsheet.html', 'text/html;charset=utf-8', cheatsheetHtml())
  }
}

/** Open the printable HTML in a new tab and invoke the print dialog. */
export function printCheatsheet(): void {
  if (typeof window === 'undefined') return
  const w = window.open('', '_blank', 'noopener,noreferrer')
  if (!w) {
    // Popup blocked — fall back to a straight HTML download the user can print.
    downloadCheatsheet('html')
    return
  }
  w.document.open()
  w.document.write(cheatsheetHtml())
  w.document.close()
  w.focus()
  // Give the new document a tick to lay out before printing.
  window.setTimeout(() => w.print(), 350)
}
